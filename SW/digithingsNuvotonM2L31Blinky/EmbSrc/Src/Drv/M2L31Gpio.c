#include "../Target/Target.h"
#include "..\M2L31_Gen\M2L31_GPIO_Def.h"

#define GPIO_INPUT_MODE_0 0
#define GPIO_OUTPUT_MODE_PUSH_PULL_1 1


void M2L31GpioInit(void) {
	volatile sM2L31_GPIO* pM2L31_GPIO = (sM2L31_GPIO*)M2L31_GPIO_ADR;
	uM2L31_GPIO_PC_MODE M2L31_GPIO_PC_MODE;
	uM2L31_GPIO_PB_MODE M2L31_GPIO_PB_MODE ;

	M2L31_GPIO_PC_MODE.All = 0;
	M2L31_GPIO_PC_MODE.Bit.MODE14 = GPIO_OUTPUT_MODE_PUSH_PULL_1; /* LED R1 */
	pM2L31_GPIO->M2L31_GPIO_PC_MODE.All = M2L31_GPIO_PC_MODE.All;

	M2L31_GPIO_PB_MODE.All = 0;
	M2L31_GPIO_PB_MODE.Bit.MODE0 = GPIO_INPUT_MODE_0;
	pM2L31_GPIO->M2L31_GPIO_PB_MODE.All = M2L31_GPIO_PB_MODE.All;

}

